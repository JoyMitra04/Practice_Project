"""
1. Addition (+)
2. Subtraction (-)
3. Divide (/)
4. Multiplication (*)
5. Reminder (%)
6. Exponent (**)
7. Floor Division (//)
"""
num1 = 10
num2 = 20

#Addition (+)
print(num1 + num2)

#Subtraction (-)
print(num2 - num1)

#Divide (/)
print(num2 / num1)

#Multiplication(*)
print(num2 * num1)

#Reminder(%)
print(num2 % num1)

#Exponent (**)
print(2 ** 3)

#Floor Division
print(num2 // num1)

